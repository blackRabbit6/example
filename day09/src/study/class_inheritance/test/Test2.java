package study.class_inheritance.test;

public class Test2 {

		private int c;
		private int d;
		
		public Test2() {}
		
		public Test2(int c, int d) {
			this.c = c;
			this.d = d;
		}

		public int getC() {
			return c;
		}

		public void setC(int c) {
			this.c = c;
		}

		public int getD() {
			return d;
		}

		public void setD(int d) {
			this.d = d;
		}
		
		


}
